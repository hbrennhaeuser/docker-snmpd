#!/usr/bin/env bash
set -euo pipefail

log() {
	printf '%s\n' "$*"
}

dbg() {
	if [ "${DEBUG:-false}" = "true" ]; then
		printf '%s\n' "$*"
	fi
}

BASE_CONFIG_FILE="/etc/snmp/snmpd.conf"
RUNTIME_CONFIG_FILE="/run/snmpd/snmpd.conf"
MOUNT_INFO="/proc/self/mountinfo"

# Configuration default values

# Enforced configuration options
export SNMPD_AGENTADDRESS="udp:0.0.0.0:10161,tcp:0.0.0.0:10161"

declare -A NAMED_OPTIONS
declare -a APPEND_LINES
declare -A V3_USERS

if ! grep -qE '[[:space:]]/host_proc[[:space:]]' "$MOUNT_INFO"; then
	log "ERROR: /host_proc is not mounted. Start container with: -v /proc:/host_proc:ro" >&2
	exit 1
fi

mkdir -p /run/snmpd

while IFS='=' read -r name value; do
	case "${name}" in
		SNMPD_CUST_*)
			[[ -n "${value}" ]] && APPEND_LINES+=("${value}")
			;;
		SNMPD_V3_*|SNMPV_V3_*)
			continue
			;;
		SNMPD_*)
			option_name="${name#SNMPD_}"
			if [[ -n "${option_name}" && -n "${value}" ]]; then
				option_name="$(printf '%s' "${option_name}" | tr '[:upper:]' '[:lower:]')"
				NAMED_OPTIONS["${option_name}"]="${value}"
			fi
			;;
	esac
done < <(env | LC_ALL=C sort)

if grep -qE "[[:space:]]${BASE_CONFIG_FILE}[[:space:]]" "${MOUNT_INFO}"; then
	log "Using mounted ${BASE_CONFIG_FILE} as base config"
else
	log "No base config found, generating config from environment"
	cat > "${RUNTIME_CONFIG_FILE}" <<'EOF'
# Generated by entrypoint from environment variables.
#
# Supported:
#   SNMPD_OPTIONNAME=value      -> optionname value
#   SNMPD_CUST_01=value         -> appended verbatim (sorted by env var name)
#
# Examples:
#   SNMPD_SYSLOCATION=Example Location Rack, Room, Building, City, Country [GPSX,Y]
#   SNMPD_CUST_10=view all           included  .1
#   SNMPD_CUST_11=view systemview    included  .1.3.6.1.2.1.1
#   SNMPD_CUST_12=view systemview    included  .1.3.6.1.2.1.25.1.1
#   SNMPD_CUST_20=rocommunity public default -V all
#
EOF
fi

if [[ -f "${BASE_CONFIG_FILE}" ]]; then
	cp "${BASE_CONFIG_FILE}" "${RUNTIME_CONFIG_FILE}"
fi

if [[ ${#NAMED_OPTIONS[@]} -gt 0 ]]; then
	for option_name in "${!NAMED_OPTIONS[@]}"; do
		dbg "Checking whether env-provided parameter '${option_name}' exists in existing config"
		if grep -qE "^[[:space:]]*${option_name}([[:space:]]|$)" "${RUNTIME_CONFIG_FILE}"; then
			log "Disabling existing '${option_name}' directive from base config (overridden by env)"
			awk -v option="${option_name}" '
				BEGIN { regex = "^[[:space:]]*" option "([[:space:]]|$)" }
				$0 ~ regex && $0 !~ /^[[:space:]]*#/ { print "# overridden by env: " $0; next }
				{ print }
			' "${RUNTIME_CONFIG_FILE}" > "${RUNTIME_CONFIG_FILE}.tmp"
			mv "${RUNTIME_CONFIG_FILE}.tmp" "${RUNTIME_CONFIG_FILE}"
		else
			dbg "No existing '${option_name}' directive found in base config"
		fi
	done
fi

{
	echo ""
	echo "# ---- begin env-managed options ----"
	if [[ ${#NAMED_OPTIONS[@]} -gt 0 ]]; then
		while IFS= read -r option_name; do
			printf '%s %s\n' "${option_name}" "${NAMED_OPTIONS[${option_name}]}"
		done < <(printf '%s\n' "${!NAMED_OPTIONS[@]}" | LC_ALL=C sort)
	fi
	echo "# ---- end env-managed options ----"

	echo ""
	echo "# ---- begin env-managed custom lines ----"
	if [[ ${#APPEND_LINES[@]} -gt 0 ]]; then
		printf '%s\n' "${APPEND_LINES[@]}"
	fi
	echo "# ---- end env-managed custom lines ----"

} >> "${RUNTIME_CONFIG_FILE}"

dbg "Assembled runtime config at ${RUNTIME_CONFIG_FILE}:"
if [ "${DEBUG:-false}" = "true" ]; then
	sed 's/^/ > /' "${RUNTIME_CONFIG_FILE}"
fi

log "Starting snmpd process"
exec /usr/local/sbin/snmpd -f -c "${RUNTIME_CONFIG_FILE}"
